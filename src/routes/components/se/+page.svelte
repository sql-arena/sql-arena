<h1>Storage Engine (SE)</h1>

<article>
<p>Here, our goal is to test how good the storage engines of various database work and what features they expose</p>

<p>Ideas being worked on:</p>

<ul>
	<li>Raw scan Speed</li>
	<li>Pushdown capbility: AND, OR, Bloom and other operators.</li>
	<li>Type support</li>
	<li>Index types and tradeoffs (bitmaps, blooms, min/max, B-trees)</li>
</ul>
